const { Sequelize } = require('sequelize');
const path = require('path');
const fs = require('fs').promises;
const config = require('../config/config.js');

const env = process.env.NODE_ENV || 'development';
const dbConfig = config[env];

const sequelize = new Sequelize(
  dbConfig.database,
  dbConfig.username,
  dbConfig.password,
  {
    host: dbConfig.host,
    dialect: dbConfig.dialect,
    logging: console.log
  }
);

async function runAllSeeders() {
  try {
    const queryInterface = sequelize.getQueryInterface();

    // Ex√©cuter les seeders de production
    console.log('üå± Ex√©cution des seeders de production...\n');
    const prodSeedersPath = path.join(__dirname, '../../seeders/prod');
    const prodSeederFiles = (await fs.readdir(prodSeedersPath))
      .filter(file => file.endsWith('.js'))
      .sort();

    for (const file of prodSeederFiles) {
      console.log(`üì¶ Ex√©cution du seeder: ${file}`);
      const seeder = require(path.join(prodSeedersPath, file));

      if (seeder.up) {
        await seeder.up(queryInterface, Sequelize);
        console.log(`‚úÖ ${file} - Succ√®s\n`);
      }
    }

    console.log('‚ú® Seeders de production termin√©s!\n');

    // Ex√©cuter les seeders de d√©veloppement
    console.log('üå± Ex√©cution des seeders de d√©veloppement...\n');
    const devSeedersPath = path.join(__dirname, '../../seeders/dev');
    const devSeederFiles = (await fs.readdir(devSeedersPath))
      .filter(file => file.endsWith('.js'))
      .sort();

    for (const file of devSeederFiles) {
      console.log(`üì¶ Ex√©cution du seeder: ${file}`);
      const seeder = require(path.join(devSeedersPath, file));

      if (seeder.up) {
        await seeder.up(queryInterface, Sequelize);
        console.log(`‚úÖ ${file} - Succ√®s\n`);
      }
    }

    console.log('‚ú® Tous les seeders ont √©t√© ex√©cut√©s avec succ√®s!');
    process.exit(0);
  } catch (error) {
    console.error('‚ùå Erreur lors de l\'ex√©cution des seeders:', error);
    process.exit(1);
  } finally {
    await sequelize.close();
  }
}

runAllSeeders();
